    private void checkTotal(){
        BigDecimal price = new BigDecimal(lblPayTotal.getText());
        BigDecimal paid = new BigDecimal(txtPay.getText());
        
        int compare = paid.compareTo(price);
        if (compare == 0|| compare ==1){
            btnConfirm.setEnabled(true);
            lblChange.setText(String.valueOf(paid.subtract(price)));
        }else{
            btnConfirm.setEnabled(false);
            lblChange.setText(null);
        }
    }
    
